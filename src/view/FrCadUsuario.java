    public boolean verificarCampos() {

        if (!txtDataNasc.getText().matches("^\\d{2}/\\d{2}/\\d{4}$")) {
            JOptionPane.showMessageDialog(null, "A data deve estar no formato dd/mm/aaaa");
            return false;
        }

        if (!txtNome.getText().matches("^[\\p{L} ]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Nome' deve conter apenas letras e espaços");
            return false;
        }

        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O Campo 'Nome' está em branco!!");
            return false;
        }

        if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O Campo 'Email' está em branco!!");
            return false;
        }

        if (!txtEmail.getText().matches("^[a-z0-9._-]+@[a-z0-9._-]+.[a-z._]+$")) {
            JOptionPane.showMessageDialog(null,
                    "O campo 'Email' possui formato inválido");
            return false;
        }

        String lSenha = new String(txtSenha.getPassword());
        String lConfirmarSenha = new String(txtConfirmarSenha.getPassword());

        if (!lSenha.equals(lConfirmarSenha)) {
            JOptionPane.showMessageDialog(null, "As senhas não são iguais.");
            return false;
        }
        if (lSenha.length() < 6) {
            JOptionPane.showMessageDialog(null, "A senha deve ter pelo menos 6 caracteres");
            return false;
        }

        if (txtNome.getText().length() < 6) {
            JOptionPane.showMessageDialog(null, "Insira seu nome e sobrenome.");
        }

        return true;
    }

