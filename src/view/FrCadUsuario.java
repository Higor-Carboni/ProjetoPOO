    public void gravar() {

        Usuario usu = new Usuario();

        String lSenha = new String(txtSenha.getPassword());
        String lHashSenha = Utils.calcularHash(lSenha);

        Date datanasc = Utils.convertStringToDate(txtDataNasc.getText());

        usu.setNome(txtNome.getText());
        usu.setEmail(txtEmail.getText());
        usu.setSenha(lHashSenha);
        usu.setAtivo(chkAtivo.isSelected());
        usu.setDatanasc(datanasc);

        UsuarioController controller = new UsuarioController();
        if (controller.inserirUsuario(usu)) {
            JOptionPane.showMessageDialog(null, "Usuario gravado com Sucesso!!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "O Usuario não foi cadastrado");
        }
    }

